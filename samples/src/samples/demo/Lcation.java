package samples.demo;

public class Lcation {
	
	private String name;
	private int age;
	private String contry;
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setAge(int age)
	{
		this.age = age;
	}
	
	public int getAge()
	{
		return age;
	}

	public void setContry(String contry)
	{
		this.contry = contry;
	}
	
	public String getContry()
	{
		return contry;
	}
}
